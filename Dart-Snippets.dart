void main() {

// Syntax for Increment (pre-increment, post-increment) & Decrement (pre-decrement, post-decrement) operators
  int a = 10;
  
  // Pre-increment
  ++a;                   // a = a + 1
  print(a);
  // post-increment
  a++;                   // a = a + 1
  print(a);
  
  // Pre - decrement
  --a;                   // a = a - 1
  print(a);
  
  // post -decrement 
  a--;                   // a = a - 1
  print(a);

}